package com.algorithms.week01_arrays_strings;

/**
 * Problem 2: Maximum Subarray
 * 
 * LeetCode #53 - Medium
 * 
 * Given an integer array nums, find the contiguous subarray (containing at least 
 * one number) which has the largest sum and return its sum.
 * 
 * A subarray is a contiguous part of an array.
 * 
 * Constraints:
 * - 1 <= nums.length <= 10^5
 * - -10^4 <= nums[i] <= 10^4
 * 
 * Example 1:
 * Input: nums = [-2,1,-3,4,-1,2,1,-5,4]
 * Output: 6
 * Explanation: [4,-1,2,1] has the largest sum = 6.
 * 
 * Example 2:
 * Input: nums = [1]
 * Output: 1
 * 
 * Example 3:
 * Input: nums = [5,4,-1,7,8]
 * Output: 23
 * 
 * Follow-up: If you have figured out the O(n) solution, try coding another solution 
 * using the divide and conquer approach, which is more subtle.
 */
public class Problem02_MaximumSubarray {
    
    /**
     * Find the maximum sum of any contiguous subarray.
     * 
     * @param nums array of integers
     * @return maximum sum of contiguous subarray
     */
    public int maxSubArray(int[] nums) {
        // Hint: Use Kadane's algorithm (dynamic programming approach)
        // Time complexity: O(n)
        // Space complexity: O(1)
        
        int maxSum = nums[0];
        int maxTillNow = nums[0];

        for (int i = 1; i < nums.length; i++) {
            maxTillNow = Math.max(nums[i], maxTillNow + nums[i]);
            maxSum = Math.max(maxSum, maxTillNow);
        }
        return maxSum;
    }
}
